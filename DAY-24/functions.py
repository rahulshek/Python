
# Introduction to functions 
# Functions are a way to group together a sequence of instructions that perform a specific task.
# Functions can be defined using the def keyword, followed by the function name and a set of parentheses.
# The body of the function is indented and contains the instructions that will be executed when the function is called.
# Functions can take parameters, which are values that are passed to the function when it is called.
# Functions can also return values, which are values that are returned to the caller when the function is finished executing.

